#!/usr/bin/env bash
#
# Run Scale Factor & Plaintext Modulus benchmark experiments.
#
# This script runs directly on EC2 #2 (compute server).
# It benchmarks N images × 5 configs (3 scale factors + 2 extra moduli).
#
# Prerequisites:
#   1. Docker container is running:  docker-compose up -d --build
#   2. Setup script was run:         ./scripts/setup_new_experiments.sh
#
# Usage:
#   chmod +x scripts/run_new_experiments.sh
#   ./scripts/run_new_experiments.sh          # default: 10 images
#   ./scripts/run_new_experiments.sh 20       # custom: 20 images
#
# Expected runtime: ~10 images × ~14s/image × 5 configs ≈ 12 minutes total
#   (though plaintext_modulus changes may affect context creation time)
#

set -euo pipefail

# Number of images to benchmark (default: 10, pass as first arg to override)
IMAGE_LIMIT="${1:-10}"

echo "================================================================"
echo "  New Experiments Benchmark Suite"
echo "  Scale Factor (3 configs) + Plaintext Modulus (3 configs)"
echo "  Images per config: $IMAGE_LIMIT"
echo "================================================================"
echo ""

# ════════════════════════════════════════════════════════════════════
# Experiment 1: Scale Factor
# ════════════════════════════════════════════════════════════════════
SCALE_DIRS=(
    "mnist_training/weights_scale100"
    "mnist_training/weights_deg2"
    "mnist_training/weights_scale10000"
)
SCALE_LABELS=("scale100" "scale1000" "scale10000")

echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "  EXPERIMENT 1: Scale Factor Comparison"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""

TOTAL_START=$(date +%s)

for i in "${!SCALE_DIRS[@]}"; do
    WDIR="${SCALE_DIRS[$i]}"
    LABEL="${SCALE_LABELS[$i]}"

    if [ ! -d "$WDIR" ]; then
        echo "SKIP: $WDIR does not exist"
        echo "  Run: ./scripts/setup_new_experiments.sh first"
        echo ""
        continue
    fi

    # Check for test images (use 10-image set if 100 not available)
    IMG_FILE=""
    if [ -f "$WDIR/test_images_100.csv" ]; then
        IMG_FILE="test_images_100.csv"
    elif [ -f "$WDIR/test_images.csv" ]; then
        IMG_FILE="test_images.csv"
    else
        echo "SKIP: No test images found in $WDIR"
        echo "  Expected: test_images_100.csv or test_images.csv"
        echo ""
        continue
    fi

    SF=$(python3 -c "import json; cfg=json.load(open('$WDIR/model_config.json')); print(cfg['quantization']['scale_factor'])")
    echo "  Running: Scale Factor = $SF ($WDIR)"
    echo "  Images: $IMG_FILE"

    START=$(date +%s)

    cargo run --release --example mnist_benchmark -- \
        --weights "$WDIR" \
        --output "mnist_training/fhe_benchmark_${LABEL}_128bit.csv" \
        --limit "$IMAGE_LIMIT"

    END=$(date +%s)
    ELAPSED=$((END - START))
    echo "  Scale=$SF completed in ${ELAPSED}s"
    echo ""
done

echo ""

# Note: the scale=1000 baseline is weights_deg2 — its CSV was already
# generated by run_all_benchmarks.sh as fhe_benchmark_deg2_128bit.csv.
# We symlink it so csv_to_json_experiments.py can find it:
if [ -f "mnist_training/fhe_benchmark_deg2_128bit.csv" ] && [ ! -f "mnist_training/fhe_benchmark_scale1000_128bit.csv" ]; then
    cp "mnist_training/fhe_benchmark_deg2_128bit.csv" "mnist_training/fhe_benchmark_scale1000_128bit.csv"
    echo "  ✓ Copied deg2 baseline CSV → fhe_benchmark_scale1000_128bit.csv"
    echo ""
fi

# ════════════════════════════════════════════════════════════════════
# Experiment 2: Plaintext Modulus
# ════════════════════════════════════════════════════════════════════
MODULUS_DIRS=(
    "mnist_training/weights_p65537"
    "mnist_training/weights_deg2"
    "mnist_training/weights_p4294967311"
)
MODULUS_LABELS=("p65537" "p100073473" "p4294967311")

echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "  EXPERIMENT 2: Plaintext Modulus Comparison"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""

for i in "${!MODULUS_DIRS[@]}"; do
    WDIR="${MODULUS_DIRS[$i]}"
    LABEL="${MODULUS_LABELS[$i]}"

    if [ ! -d "$WDIR" ]; then
        echo "SKIP: $WDIR does not exist"
        echo "  Run: ./scripts/setup_new_experiments.sh first"
        echo ""
        continue
    fi

    # Check for test images
    IMG_FILE=""
    if [ -f "$WDIR/test_images_100.csv" ]; then
        IMG_FILE="test_images_100.csv"
    elif [ -f "$WDIR/test_images.csv" ]; then
        IMG_FILE="test_images.csv"
    else
        echo "SKIP: No test images found in $WDIR"
        echo ""
        continue
    fi

    PM=$(python3 -c "import json; cfg=json.load(open('$WDIR/model_config.json')); print(cfg['quantization']['plaintext_modulus'])")
    echo "  Running: Plaintext Modulus = $PM ($WDIR)"
    echo "  Images: $IMG_FILE"

    START=$(date +%s)

    cargo run --release --example mnist_benchmark -- \
        --weights "$WDIR" \
        --output "mnist_training/fhe_benchmark_${LABEL}_128bit.csv" \
        --limit "$IMAGE_LIMIT"

    END=$(date +%s)
    ELAPSED=$((END - START))
    echo "  Modulus=$PM completed in ${ELAPSED}s"
    echo ""
done

# Copy baseline for modulus experiment too
if [ -f "mnist_training/fhe_benchmark_deg2_128bit.csv" ] && [ ! -f "mnist_training/fhe_benchmark_p100073473_128bit.csv" ]; then
    cp "mnist_training/fhe_benchmark_deg2_128bit.csv" "mnist_training/fhe_benchmark_p100073473_128bit.csv"
    echo "  ✓ Copied deg2 baseline CSV → fhe_benchmark_p100073473_128bit.csv"
    echo ""
fi

TOTAL_END=$(date +%s)
TOTAL_ELAPSED=$((TOTAL_END - TOTAL_START))

echo "================================================================"
echo "  All new experiments complete!"
echo "  Total time: ${TOTAL_ELAPSED}s ($((TOTAL_ELAPSED / 60))m $((TOTAL_ELAPSED % 60))s)"
echo "================================================================"
echo ""
echo "  Scale Factor Results:"
ls -la mnist_training/fhe_benchmark_scale*_128bit.csv 2>/dev/null || echo "  (no scale factor CSVs found)"
echo ""
echo "  Plaintext Modulus Results:"
ls -la mnist_training/fhe_benchmark_p*_128bit.csv 2>/dev/null || echo "  (no modulus CSVs found)"
echo ""
echo "  Next step: run  python3 scripts/csv_to_json_experiments.py"
echo "  Then copy the JSON files to the frontend EC2."
